let day = "Wednesday"

switch(day){
    case "monday":
        console.log("day is monday");
        break;
    case "tuesday":
        console.log("day is tuesday");
        break;
    case "Wednesday":
        console.log("day is Wednesday");
        break;
    case "Thursday":
        console.log("day is Thursday");
        break;
    case "Friday":
        console.log("day is Friday");
        break;
    case "saturday":
        console.log("day is saturday");
        break;
    default:
        console.log("the day is weekend");
        break;

}